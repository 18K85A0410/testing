package Maths;

public class Maths {
	public void add(int a, int b) {
		int c = a+b;
		System.out.println("The addition of two numbers is "+a+"+"+b+"="+c);
	}
	public void sub(int x, int y) {
		int z = x-y;
		System.out.println("The subtraction of two numbers is "+x+"-"+y+"="+z);
	}
	public void div(int c, int d) {
		int e = c/d;
		System.out.println("The divison of two numbers is "+c+"+"+d+"="+e);
	}
	public void mul(int s, int t) {
		int u = s*t;
		System.out.println("The multiplication of two numbers is"+s+"*"+t+"="+u);
	}

}
